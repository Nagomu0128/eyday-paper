export class DrizzleTagRepository implements TagRepository {
  constructor(private readonly db: Database) {}

  async upsert(input: {
    id: string;
    userId: string;
    name: string;
    kind: Tag["kind"];
  }): Promise<Tag> {
    await this.db
      .insert(tag)
      .values(input)
      .onConflictDoNothing({ target: [tag.userId, tag.name, tag.kind] });
    const rows = await this.db
      .select()
      .from(tag)
      .where(and(eq(tag.userId, input.userId), eq(tag.name, input.name), eq(tag.kind, input.kind)));
    const row = rows[0];
    if (!row) throw new AppError("internal", "tag upsert returned no row");
    return row;
  }

  async list(userId: string): Promise<Tag[]> {
    return this.db.select().from(tag).where(eq(tag.userId, userId)).orderBy(asc(tag.name));
  }

  async attachToPaper(userId: string, paperId: string, tagId: string): Promise<void> {
    await this.assertOwned(userId, paperId, tagId);
    await this.db.insert(paperTag).values({ paperId, tagId }).onConflictDoNothing();
  }

  async detachFromPaper(userId: string, paperId: string, tagId: string): Promise<void> {
    await this.assertOwned(userId, paperId, tagId);
    await this.db
      .delete(paperTag)
      .where(and(eq(paperTag.paperId, paperId), eq(paperTag.tagId, tagId)));
  }

  async listForPaper(userId: string, paperId: string): Promise<Tag[]> {
    const rows = await this.db
      .select({ id: tag.id, userId: tag.userId, name: tag.name, kind: tag.kind })
      .from(paperTag)
      .innerJoin(tag, eq(paperTag.tagId, tag.id))
      .where(and(eq(tag.userId, userId), eq(paperTag.paperId, paperId)))
      .orderBy(asc(tag.name));
    return rows;
  }

  private async assertOwned(userId: string, paperId: string, tagId: string): Promise<void> {
    const p = await this.db
      .select({ id: paper.id })
      .from(paper)
      .where(and(eq(paper.userId, userId), eq(paper.id, paperId)));
    const t = await this.db
      .select({ id: tag.id })
      .from(tag)
      .where(and(eq(tag.userId, userId), eq(tag.id, tagId)));
    if (!p[0] || !t[0]) {
      throw new AppError("forbidden", "paper or tag does not belong to user");
    }
  }
}

// ---------- Chunk ----------

export class DrizzleChunkRepository implements ChunkRepository {
  constructor(private readonly db: Database) {}

  async bulkCreate(chunks: NewChunk[]): Promise<void> {
    if (chunks.length === 0) return;
    await this.db.insert(chunk).values(
      chunks.map((c) => ({
        id: c.id,
        userId: c.userId,
        paperId: c.paperId,
        idx: c.idx,
        section: c.section ?? null,
        page: c.page ?? null,
        vectorId: c.vectorId ?? null,
        text: c.text ?? "",
        charLen: c.charLen ?? 0,
      })),
    );
  }

  async listByPaper(userId: string, paperId: string): Promise<Chunk[]> {
    return this.db
      .select()
      .from(chunk)
      .where(and(eq(chunk.userId, userId), eq(chunk.paperId, paperId)))
      .orderBy(asc(chunk.idx));
  }

  async findByIds(userId: string, ids: string[]): Promise<Chunk[]> {
    if (ids.length === 0) return [];
    return this.db
      .select()
      .from(chunk)
      .where(and(eq(chunk.userId, userId), inArray(chunk.id, ids)));
  }

  async deleteByPaper(userId: string, paperId: string): Promise<void> {
    await this.db.delete(chunk).where(and(eq(chunk.userId, userId), eq(chunk.paperId, paperId)));
  }

  async countByPaper(userId: string, paperId: string): Promise<number> {
    const rows = await this.db
      .select({ value: count() })
      .from(chunk)
      .where(and(eq(chunk.userId, userId), eq(chunk.paperId, paperId)));
    return rows[0]?.value ?? 0;
  }
}

