// ---------- Paper ----------

type PaperRow = typeof paper.$inferSelect;

const toPaper = (r: PaperRow): Paper => ({
  id: r.id,
  userId: r.userId,
  title: r.title,
  authors: JSON.parse(r.authorsJson) as string[],
  year: r.year,
  venue: r.venue,
  doi: r.doi,
  arxivId: r.arxivId,
  sourceUrl: r.sourceUrl,
  abstract: r.abstract,
  langDetected: r.langDetected,
  status: r.status,
  readabilityHint: r.readabilityHint,
  primaryFolderId: r.primaryFolderId,
  pdfR2Key: r.pdfR2Key,
  textR2Key: r.textR2Key,
  addedAt: r.addedAt,
  updatedAt: r.updatedAt,
});

export class DrizzlePaperRepository implements PaperRepository {
  constructor(private readonly db: Database) {}

  async create(input: NewPaper): Promise<Paper> {
    const rows = await this.db
      .insert(paper)
      .values({
        id: input.id,
        userId: input.userId,
        title: input.title,
        authorsJson: JSON.stringify(input.authors ?? []),
        year: input.year ?? null,
        venue: input.venue ?? null,
        doi: input.doi ?? null,
        arxivId: input.arxivId ?? null,
        sourceUrl: input.sourceUrl ?? null,
        abstract: input.abstract ?? null,
        langDetected: input.langDetected ?? null,
        primaryFolderId: input.primaryFolderId ?? null,
        pdfR2Key: input.pdfR2Key ?? null,
        textR2Key: input.textR2Key ?? null,
      })
      .returning();
    const row = rows[0];
    if (!row) throw new AppError("internal", "paper insert returned no row");
    return toPaper(row);
  }

  async findById(userId: string, id: string): Promise<Paper | null> {
    const rows = await this.db
      .select()
      .from(paper)
      .where(and(eq(paper.userId, userId), eq(paper.id, id)));
    return rows[0] ? toPaper(rows[0]) : null;
  }

  async findByArxivId(userId: string, arxivId: string): Promise<Paper | null> {
    const rows = await this.db
      .select()
      .from(paper)
      .where(and(eq(paper.userId, userId), eq(paper.arxivId, arxivId)));
    return rows[0] ? toPaper(rows[0]) : null;
  }

  async findByDoi(userId: string, doi: string): Promise<Paper | null> {
    const rows = await this.db
      .select()
      .from(paper)
      .where(and(eq(paper.userId, userId), eq(paper.doi, doi)));
    return rows[0] ? toPaper(rows[0]) : null;
  }

  async list(userId: string, options?: ListPapersOptions): Promise<Paper[]> {
    const conds = [eq(paper.userId, userId)];
    if (options?.status) conds.push(eq(paper.status, options.status));
    if (options?.folderId) conds.push(eq(paper.primaryFolderId, options.folderId));
    const rows = await this.db
      .select()
      .from(paper)
      .where(and(...conds))
      .orderBy(desc(paper.addedAt))
      .limit(options?.limit ?? 200)
      .offset(options?.offset ?? 0);
    return rows.map(toPaper);
  }

  async update(userId: string, id: string, patch: PaperPatch): Promise<void> {
    await this.db
      .update(paper)
      .set({ ...patch, updatedAt: new Date() })
      .where(and(eq(paper.userId, userId), eq(paper.id, id)));
  }

  async setStatus(userId: string, id: string, status: PaperStatus): Promise<void> {
    await this.db
      .update(paper)
      .set({ status, updatedAt: new Date() })
      .where(and(eq(paper.userId, userId), eq(paper.id, id)));
  }

  async setPrimaryFolder(userId: string, id: string, folderId: string | null): Promise<void> {
    await this.db
      .update(paper)
      .set({ primaryFolderId: folderId, updatedAt: new Date() })
      .where(and(eq(paper.userId, userId), eq(paper.id, id)));
  }

  async delete(userId: string, id: string): Promise<void> {
    await this.db.delete(paper).where(and(eq(paper.userId, userId), eq(paper.id, id)));
  }
}

// ---------- Folder ----------

export class DrizzleFolderRepository implements FolderRepository {
  constructor(private readonly db: Database) {}

  async create(input: {
    id: string;
    userId: string;
    name: string;
    parentId?: string | null;
    autoGenerated?: boolean;
  }): Promise<Folder> {
    const rows = await this.db
      .insert(folder)
      .values({
        id: input.id,
        userId: input.userId,
        name: input.name,
        parentId: input.parentId ?? null,
        autoGenerated: input.autoGenerated ?? true,
      })
      .returning();
    const row = rows[0];
    if (!row) throw new AppError("internal", "folder insert returned no row");
    return row;
  }

  async findById(userId: string, id: string): Promise<Folder | null> {
    const rows = await this.db
      .select()
      .from(folder)
      .where(and(eq(folder.userId, userId), eq(folder.id, id)));
    return rows[0] ?? null;
  }

  async list(userId: string): Promise<Folder[]> {
    return this.db.select().from(folder).where(eq(folder.userId, userId)).orderBy(asc(folder.name));
  }

  async listChildren(userId: string, parentId: string | null): Promise<Folder[]> {
    const cond =
      parentId === null
        ? and(eq(folder.userId, userId), isNull(folder.parentId))
        : and(eq(folder.userId, userId), eq(folder.parentId, parentId));
    return this.db.select().from(folder).where(cond).orderBy(asc(folder.name));
  }

  async rename(userId: string, id: string, name: string): Promise<void> {
    await this.db
      .update(folder)
      .set({ name })
      .where(and(eq(folder.userId, userId), eq(folder.id, id)));
  }

  async delete(userId: string, id: string): Promise<void> {
    await this.db.delete(folder).where(and(eq(folder.userId, userId), eq(folder.id, id)));
  }
}

